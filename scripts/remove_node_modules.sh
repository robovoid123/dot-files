#! /bin/bash

delete_nm() {
    if [[ -d $1 ]]; then
        if [[ -d $1/node_modules ]]; then
            echo "deleting node_modules from $1"
            rm -rf $1/node_modules
        fi
        for fd in `ls $1`; do
        delete_nm $1/$fd
        done
    fi
}

for folder in $*; do
delete_nm $folder
done
